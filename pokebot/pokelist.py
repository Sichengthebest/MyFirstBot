class Pokemon():
    id = ''
    name = ''
    hp = 0
    lvl = 0
    atk = 0
    catchrate = []
    pktype = []
    upgrade = ''
    speed = 0
    xp = 0

    def getPhoto(self):
        file_name = f"{pokeconfig.run_path}/images/{self.id}.jpg"
        return file_name

    def __init__(self,id:str,xp:int):
        self.id = id
        self.name = pokemon[id]['name']
        self.lvl = int(xp/1000)+1
        maxhp = random.randint(pokemon[id]['hp'][0],pokemon[id]['hp'][1])
        lvlnum = pokemon[id]['lvl'][1] - pokemon[id]['lvl'][0]
        hpperlvl = int(maxhp / lvlnum)
        self.hp = hpperlvl * self.lvl
        maxatk = random.randint(pokemon[id]['atk'][0],pokemon[id]['atk'][1])
        atkperlvl = int(maxatk / lvlnum)
        self.atk = atkperlvl * self.lvl
        maxspeed = random.randint(pokemon[id]['speed'][0],pokemon[id]['speed'][1])
        spdperlvl = int(maxspeed / lvlnum)
        self.speed = spdperlvl * self.lvl
        self.catchrate = pokemon[id]['Catch rate']
        self.pktype = pokemon[id]['pktype']
        self.upgrade = random.choice(pokemon[id]['upgrade'])
        self.xp = xp
        self.evolvewith = pokemon[id]['evolvewith']
    
    @classmethod
    def init(self,id:str,name:str,lvl:int,hp:int,atk:int,pktype:str,upgrade:str,speed:int,xp:int,evolvewith:str):
        p = Pokemon(id,lvl)
        p.hp = hp
        p.atk = atk
        p.pktype = pktype
        p.upgrade = upgrade
        p.speed = speed
        p.evolvewith = evolvewith
        return p
    
    @classmethod
    def init_from_dict(self,pdict):
        p = Pokemon(pdict['id'],pdict['lvl'])
        p.hp = pdict['hp']
        p.atk = pdict['atk']
        p.pktype = pdict['pktype']
        p.upgrade = pdict['upgrade']
        p.speed = pdict['speed']
        p.xp = pdict['xp']
        p.evolvewith = pdict['evolvewith']
        return p

    def __str__(self):
        return str(self.__dict__)