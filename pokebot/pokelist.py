class Pokemon():
    id = ''
    name = ''
    hp = 0
    currhp = 0
    lvl = 0
    atk = 0
    defence = 0
    catchrate = []
    pktype = []
    upgrade = ''
    speed = 0
    xp = 0
    evolvewith = ''
    friendship = 0

    def getPhoto(self):
        file_name = f"{pokeconfig.run_path}/images/{self.id}.jpg"
        return file_name

    def __init__(self,id:str,xp:int,currfr,moves):
        self.id = id
        self.name = pokemon[id]['name']
        self.lvl = int(xp/1000)+1
        maxhp = random.randint(pokemon[id]['hp'][0],pokemon[id]['hp'][1])
        hpperlvl = maxhp / 100
        self.hp = int(hpperlvl * self.lvl)
        if self.hp < 1:
            self.hp = 1
        self.currhp = self.hp
        maxatk = random.randint(pokemon[id]['atk'][0],pokemon[id]['atk'][1])
        atkperlvl = maxatk / 100
        self.atk = int(atkperlvl * self.lvl)
        maxspeed = random.randint(pokemon[id]['speed'][0],pokemon[id]['speed'][1])
        spdperlvl = maxspeed / 100
        self.speed = int(spdperlvl * self.lvl)
        maxdef = random.randint(pokemon[id]['def'][0],pokemon[id]['def'][1])
        defperlvl = maxdef / 100
        self.defence = int(defperlvl * self.lvl)
        self.catchrate = pokemon[id]['Catch rate']
        self.pktype = pokemon[id]['pktype']
        self.upgrade = random.choice(pokemon[id]['upgrade'])
        self.xp = xp
        self.evolvewith = pokemon[id]['evolvewith']
        if currfr == -1:
            self.friendship = pokemon[id]['friendship']
        else:
            self.friendship = currfr
        if moves == []:
            self.moves = self.get_moves(self.id,self.lvl)
        else:
            self.moves = moves
    
    @classmethod
    def init(self,id:str,name:str,lvl:int,hp:int,atk:int,pktype:str,upgrade:str,defence:int,speed:int,xp:int,evolvewith:str,friendship:int):
        p = Pokemon(id,xp,friendship,[])
        p.hp = hp
        p.atk = atk
        p.pktype = pktype
        p.upgrade = upgrade
        p.speed = speed
        p.evolvewith = evolvewith
        p.defence = defence
        return p
    
    @classmethod
    def init_from_dict(self,pdict):
        p = Pokemon(pdict['id'],pdict['lvl'],pdict['friendship'],pdict['moves'])
        p.hp = pdict['hp']
        p.atk = pdict['atk']
        p.pktype = pdict['pktype']
        p.upgrade = pdict['upgrade']
        p.speed = pdict['speed']
        p.xp = pdict['xp']
        p.evolvewith = pdict['evolvewith']
        p.defence = pdict['def']
        return p

    @classmethod
    def get_moves(self,id:str,lvl:int):
        availmoves = []
        for pkid in pokemoves.compatible:
            if pkid == id:
                for move in pokemoves.compatible[id]:
                    if pokemoves.compatible[id][move] <= lvl:
                        availmoves.append(move)
        try:
            moves = random.sample(availmoves,k=2)
        except ValueError:
            return []
        return moves
    
    def __str__(self):
        return str(self.__dict__)