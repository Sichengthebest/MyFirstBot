def getPokemon():
    rarityroll = random.randint(1,10000)
    if rarityroll <= 5000:
        pokemons = rarity['c']
        rarityy = 'c'
    elif rarityroll > 5000 and rarityroll <= 7600:
        pokemons = rarity['u']
        rarityy = 'u'
    elif rarityroll > 7600 and rarityroll <= 9500:
        pokemons = rarity['r']
        rarityy = 'r'
    elif rarityroll > 9500 and rarityroll <= 9970:
        pokemons = rarity['s']
        rarityy = 's'
    elif rarityroll > 9970 and rarityroll <= 9999:
        pokemons = rarity['l']
        rarityy = 'l'
    elif rarityroll == 10000:
        pokemons = rarity['ss']
        rarityy = 'ss'
    pk = random.choice(pokemons)
    return pk,rarityy

